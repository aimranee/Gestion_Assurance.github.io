    private void jButton10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton10MouseClicked

        if (numD.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Misssing Information *NumD*!!");
        }else{
        
        try{  

           String durre = durr.getSelectedItem().toString();
           String typee = type.getSelectedItem().toString();
           String tcaa = tca.getSelectedItem().toString();
           
           String n = numD.getText();
           String a = tn.getText();	
           String b = tp.getText();
           String c = tc.getText();
           String d = td.getText();	
           String e = tm.getText();
           String f = tg.getText();
           String g = tv.getText();
           String j = tno.getText();
           String k = tpu.getText();
           String l = tnp.getText();
           
           double Prix =CalPrix(durre, typee);
                      
           String query1="update client set nom = '"+a+"', prenom = '"+b+"', CIN = '"+c+"' where numd = "+n+"";
           stm.executeUpdate(query1);
           String query2="update contra set assurance = '"+typee+"',date = '"+d+"', durre = '"+durre+"', prix = "+Prix+" where numd = '"+n+"'";
           stm.executeUpdate(query2);
           String query3="update voiture set Marque = '"+e+"', genre = '"+f+"', modele = '"+g+"', typecarb = '"+tcaa+"', nombrecyl = '"+j+"', place = '"+l+"', puissance = '"+k+"' where numd = '"+n+"'";
           stm.executeUpdate(query3);
           
           JOptionPane.showMessageDialog(this, "Employee Updated Successfully!!");
           this.dispose();
        
        }catch(Exception e){
        JOptionPane.showMessageDialog(this, "Employee Not Updated");
        }       
        
        }
        
    }//GEN-LAST:event_jButton10MouseClicked

