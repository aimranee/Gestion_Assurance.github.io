    public int CalPrix(String durré, String type){
        int prix = 10000;
        if (durré == "3 mois")
            prix = prix - 2000;
        if (durré == "6 mois")
            prix = prix - 1000;
        if (type == "Intermédiare")
            prix = prix - 4000;
        if (type == "Tiers")
            prix = prix - 6000;
                    
        return prix;
    }
    
